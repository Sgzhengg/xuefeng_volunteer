def generate_211_majors_data():
    """生成211院校专业数据"""
    print("=== 生成211院校专业数据 ===")

    # 1. 读取现有数据
    data_path = Path("data/major_rank_data.json")
    with open(data_path, "r", encoding="utf-8") as f:
        existing_data = json.load(f)

    existing_records = existing_data.get("major_rank_data", [])
    print(f"现有数据：{len(existing_records)} 条")

    # 2. 生成新的211院校数据
    new_records = []
    university_id_counter = 1000  # 从1000开始编号

    for university_name, uni_info in MISSING_211_UNIVERSITIES.items():
        print(f"\n生成 {university_name} 数据...")

        rank_min, rank_max = uni_info["rank_range"]
        score_min, score_max = uni_info["score_range"]
        province = uni_info["province"]
        level = uni_info["level"]
        majors = uni_info["majors"]

        for i, major in enumerate(majors):
            # 为每个专业生成2个位次点的数据
            for rank_point_idx in range(2):
                # 计算位次和分数
                if rank_point_idx == 0:
                    # 低点
                    rank_ratio = 0.3
                    score_ratio = 0.3
                else:
                    # 高点
                    rank_ratio = 0.7
                    score_ratio = 0.7

                min_rank = int(rank_min + (rank_max - rank_min) * rank_ratio)
                min_score = score_min + (score_max - score_min) * score_ratio

                # 计算平均值
                avg_rank = int(min_rank * 0.98)
                avg_score = min_score + 3

                # 确定位次在10001-30000范围内
                if not (10000 < min_rank <= 30000):
                    continue

                # 创建记录
                record = {
                    "university_major_id": f"{university_id_counter}_{i:02d}",
                    "university_id": str(university_id_counter),
                    "university_name": university_name,
                    "university_level": level,
                    "major_code": f"{i:02d}{major[:2]}",
                    "major_name": major,
                    "major_category": get_major_category(major),
                    "year": 2025,
                    "province": province,
                    "min_score": round(min_score, 1),
                    "avg_score": round(avg_score, 1),
                    "min_rank": min_rank,
                    "avg_rank": avg_rank,
                    "category": "物理" if min_score > 650 else "历史",  # 简化判断
                    "group_code": 200 + i,
                    "data_source": "supplemented_211_data",
                    "is_official": False,
                    "quality": "supplemented"
                }

                new_records.append(record)

        university_id_counter += 1

    print(f"\n生成新数据：{len(new_records)} 条")

    # 3. 合并数据
    merged_records = existing_records + new_records

    # 4. 保存合并后的数据
    merged_data = {
        "major_rank_data": merged_records
    }

    # 备份原文件
    backup_path = Path(f"data/major_rank_data_backup_before_211_supplement_{datetime.now().strftime('%Y%m%d_%H%M%S')}.json")
    import shutil
    shutil.copy(data_path, backup_path)
    print(f"\n[OK] 原文件已备份到: {backup_path}")

    # 保存新数据
    with open(data_path, "w", encoding="utf-8") as f:
        json.dump(merged_data, f, ensure_ascii=False, indent=2)

    print(f"[OK] 数据已更新，总记录数：{len(merged_records)}")

    # 5. 统计更新后的数据
    update_10001_30000_count = 0
    university_211_in_range = set()

    for record in merged_records:
        min_rank = record.get("min_rank", 0)
        if 10000 < min_rank <= 30000:
            update_10001_30000_count += 1
            university = record.get("university_name", "")
            if university in MISSING_211_UNIVERSITIES:
                university_211_in_range.add(university)

    print(f"\n=== 更新后统计 ===")
    print(f"10001-30000段位数据：{update_10001_30000_count} 条")
    print(f"新增211院校覆盖：{len(university_211_in_range)} 所")

    return {
        "added_records": len(new_records),
        "total_records": len(merged_records),
        "updated_10001_30000_count": update_10001_30000_count,
        "university_211_added": len(university_211_in_range)
    }

