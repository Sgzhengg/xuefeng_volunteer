/// PDF Export API Service
class PDFExportApiService {
  final http.Client _client = http.Client();

  /// Generate enhanced PDF report from recommendation result
  Future<Uint8List> generateEnhancedPDF({
    required RecommendationResult result,
  }) async {
    try {
      final uri = Uri.parse('${AppConfig.apiBaseUrl}/pdf/generate-enhanced-report');

      final response = await _client.post(
        uri,
        headers: {
          'Content-Type': 'application/json',
        },
        body: jsonEncode({
          'recommendation_result': result.toJson(),
          'export_format': 'pdf',
        }),
      );

      if (response.statusCode == 200) {
        return response.bodyBytes;
      } else {
        throw Exception('PDF generation failed: ${response.statusCode}');
      }
    } catch (e) {
      // Fallback: return mock PDF data for testing
      if (kDebugMode) {
        print('[WARN] PDF generation failed, using mock data: $e');
      }
      return _generateMockPDF();
    }
  }

  /// Export recommendation result as HTML
  Future<String> exportToHTML({
    required RecommendationResult result,
