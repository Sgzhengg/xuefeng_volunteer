        uri,
        headers: {
          'Content-Type': 'application/json',
        },
        body: jsonEncode({
          'recommendation_result': result.toJson(),
          'export_format': 'html',
        }),
      );

      if (response.statusCode == 200) {
        return response.body;
      } else {
        throw Exception('HTML export failed: ${response.statusCode}');
      }
    } catch (e) {
      // Fallback: return mock HTML for testing
      if (kDebugMode) {
        print('[WARN] HTML export failed, using mock data: $e');
      }
      return _generateMockHTML(result);
    }
  }

